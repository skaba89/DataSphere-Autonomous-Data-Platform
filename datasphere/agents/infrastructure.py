    def _terraform_vars(self, c: ArchitectureConstraints) -> str:
        cloud = c.cloud_provider
        base = 'variable "environment" { default = "production" }\n'
        if cloud == "aws":
            return base + 'variable "region" { default = "eu-west-1" }\nvariable "tf_state_bucket" {}\n'
        if cloud == "azure":
            return base + 'variable "subscription_id" {}\nvariable "location" { default = "West Europe" }\n'
        if cloud == "gcp":
            return base + 'variable "project" {}\nvariable "region" { default = "europe-west1" }\n'
        return base

    def _terraform_outputs(self, c: ArchitectureConstraints) -> str:
        cloud = c.cloud_provider
        if cloud == "aws":
            return 'output "data_lake_bucket" { value = aws_s3_bucket.data_lake.bucket }\n'
        if cloud == "azure":
            return 'output "adls_account" { value = azurerm_storage_account.data_lake.name }\n'
        if cloud == "gcp":
            return 'output "gcs_bucket" { value = google_storage_bucket.data_lake.name }\n'
        return ""

    def _tfvars_example(self, c: ArchitectureConstraints) -> str:
        cloud = c.cloud_provider
        lines = ['environment = "production"']
        if cloud == "aws":
            lines += ['region = "eu-west-1"', 'tf_state_bucket = "my-terraform-state-bucket"']
        if cloud == "azure":
            lines += ['subscription_id = "00000000-0000-0000-0000-000000000000"', 'location = "West Europe"']
        if cloud == "gcp":
            lines += ['project = "my-gcp-project"', 'region = "europe-west1"']
        return "\n".join(lines) + "\n"
