from __future__ import annotations
from datasphere.agents.base_agent import BaseAgent
from datasphere.models.request import BusinessRequest, ArchitectureConstraints
from datasphere.models.output import AgentOutput, InfrastructureOutput


class InfrastructureAgent(BaseAgent):
    name = "infrastructure"
    description = "Génère Docker Compose, Helm values ou Terraform selon la stack choisie."

    def _run(self, request: BusinessRequest, context: dict) -> InfrastructureOutput:
        c = self._constraints(request)
        depl = c.deployment.lower().replace(" ", "-")

        artifacts: dict[str, str] = {}

        if "kubernetes" in depl or "helm" in depl:
            artifacts["helm/values.yaml"] = self._helm_values(c)
            artifacts["helm/values.override.yaml"] = self._helm_override(c)
            artifacts["k8s/namespace.yaml"] = self._k8s_namespace()
            artifacts["k8s/secrets.yaml"] = self._k8s_secrets(c)
            deployment_type = "kubernetes/helm"
        elif "terraform" in depl or (c.iac and "terraform" in c.iac.lower()):
            artifacts["terraform/main.tf"] = self._terraform_main(c)
            artifacts["terraform/variables.tf"] = self._terraform_vars(c)
            artifacts["terraform/outputs.tf"] = self._terraform_outputs(c)
            artifacts["terraform/terraform.tfvars.example"] = self._tfvars_example(c)
            deployment_type = "terraform"
        else:
            artifacts["docker-compose.yml"] = self._docker_compose(c)
            artifacts[".env.example"] = self._env_example(c)
            artifacts["Makefile"] = self._makefile(c)
            deployment_type = "docker-compose"

        output = InfrastructureOutput(deployment_type=deployment_type)
        output.artifacts = artifacts
        return output

    # ------------------------------------------------------------------
    # Docker Compose
    # ------------------------------------------------------------------

    def _docker_compose(self, c: ArchitectureConstraints) -> str:
        services: list[str] = []
        volumes: list[str] = []

        services.append(self._svc_warehouse(c))
        volumes.append(f"  {c.data_warehouse}_data:")

        services.append(self._svc_orchestrator(c))
        if c.orchestrator == "airflow":
            volumes.append("  airflow_logs:")

        if c.data_lake and c.data_lake not in ("none", "s3", "adls", "gcs"):
            services.append(self._svc_minio())
            volumes.append("  minio_data:")

        services.append(self._svc_bi(c))
        volumes.append(f"  {c.bi_tool}_data:")

        if c.catalog:
            services.append(self._svc_catalog(c))

        services.append(self._svc_monitoring(c))
        volumes += ["  prometheus_data:", "  grafana_data:"]

        if "vault" in [s.lower() for s in c.security]:
            services.append(self._svc_vault())
            volumes.append("  vault_data:")

        return "\n".join([
            "version: '3.9'",
            "",
            "# Auto-generated by DataSphere Infrastructure Agent",
            f"# Stack: {c.cloud_provider} | {c.data_warehouse} | {c.orchestrator} | {c.bi_tool}",
            "",
            "services:",
            *services,
            "",
            "volumes:",
            *volumes,
            "",
            "networks:",
            "  datasphere:",
            "    driver: bridge",
        ])

    def _svc_warehouse(self, c: ArchitectureConstraints) -> str:
        wh = c.data_warehouse
        if wh == "postgresql":
            return """  postgresql:
    image: postgres:16-alpine
    environment:
      POSTGRES_DB: ${POSTGRES_DB:-datasphere}
      POSTGRES_USER: ${POSTGRES_USER:-datasphere}
      POSTGRES_PASSWORD: ${POSTGRES_PASSWORD:?required}
    ports:
      - "5432:5432"
    volumes:
      - postgresql_data:/var/lib/postgresql/data
    healthcheck:
      test: ["CMD-SHELL", "pg_isready -U ${POSTGRES_USER:-datasphere}"]
      interval: 10s
      retries: 5
    networks: [datasphere]
    restart: unless-stopped"""

        if wh == "clickhouse":
            return """  clickhouse:
    image: clickhouse/clickhouse-server:23.8
    environment:
      CLICKHOUSE_DB: datasphere
      CLICKHOUSE_USER: ${CLICKHOUSE_USER:-datasphere}
      CLICKHOUSE_PASSWORD: ${CLICKHOUSE_PASSWORD:?required}
    ports:
      - "8123:8123"
      - "9000:9000"
    volumes:
      - clickhouse_data:/var/lib/clickhouse
    ulimits:
      nofile: {soft: 262144, hard: 262144}
    networks: [datasphere]
    restart: unless-stopped"""

        # Snowflake / BigQuery / Redshift etc. are SaaS — no local container
        return f"""  # {wh} est un service SaaS — pas de container local
  # Configurez les credentials dans .env"""

