    def _env_example(self, c: ArchitectureConstraints) -> str:
        lines = [
            "# DataSphere — Variables d'environnement",
            "# Copiez ce fichier en .env et remplissez les valeurs",
            "",
            "# Database",
            "POSTGRES_DB=datasphere",
            "POSTGRES_USER=datasphere",
            "POSTGRES_PASSWORD=changeme_strong_password",
            "",
        ]
        if c.orchestrator == "airflow":
            lines += ["# Airflow", "AIRFLOW_SECRET_KEY=changeme_32chars_minimum", ""]
        if c.data_lake and c.data_lake not in ("none", "s3", "adls", "gcs"):
            lines += ["# MinIO", "MINIO_ROOT_USER=minioadmin", "MINIO_ROOT_PASSWORD=changeme", ""]
        if c.bi_tool == "superset":
            lines += ["# Superset", "SUPERSET_SECRET_KEY=changeme_32chars_minimum", ""]
        if "vault" in [s.lower() for s in c.security]:
            lines += ["# Vault", "VAULT_TOKEN=root-dev-token", ""]
        if c.data_warehouse == "snowflake":
            lines += ["# Snowflake", "SNOWFLAKE_ACCOUNT=", "SNOWFLAKE_USER=", "SNOWFLAKE_PASSWORD=", "SNOWFLAKE_WAREHOUSE=", "SNOWFLAKE_DATABASE=datasphere", ""]
        if c.data_warehouse == "bigquery":
            lines += ["# BigQuery", "GOOGLE_APPLICATION_CREDENTIALS=/secrets/sa.json", "GCP_PROJECT=", ""]
        lines += ["# Monitoring", "GRAFANA_PASSWORD=changeme"]
        return "\n".join(lines)

